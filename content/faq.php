<h3>
	Frequently Asked Questions
</h3>

<p>For technical support and questions not covered in the FAQ, please contact textbook@pennua.org. </p>
    <p><strong> Why buy or sell your textbooks through PBB?</strong><br/>
COMPETITIVE PRICES buy at market rates and avoid middleman costs<br/>
PROTECT THE ENVIRONMENT reuse books and cut shipping-generated greenhouse gases.<br/>
EFFICIENCY You don't have to pay for shipping or wait two weeks for a book to come in the mail.<br/>
SAFETY trade with fellow students and inspect a book before paying</p>
<p><strong>Why must I register for an account in order to use the textbook exchange?</strong><br/>
Account registration ensures that only Penn-affiliated individuals can post books and contact other users, keeping the site safer and spam-free. </p>
<p><strong>What happens if I feel I've been ripped off?</strong> <br/>
Penn Book Bazaar only connects buyers and sellers, and has neither control nor responsibility over the actual transaction and sale of a book. What grievances you may have as a buyer or seller is solely between you and your other transactor. It is the buyer's responsibility to check that their book is correct and in the proper condition.</p>
<p><strong> My class is cross-listed in multiple departments. Which one should I post to? Should I make multiple posts? </strong>
Make only one post to the most fitting department (if you can't decide, pick the first one in alphabetical order). Make sure you list all the cross-listed course numbers in your post, so users searching under a different course number will be able to find your book.</p>
<p><strong> How do you find a book's ISBN?</strong><br/>
The ISBN is usually located above the bar code of the book or the 13-digit bar code itself. It can also be found in the first few pages of the book.</p>
<p><strong>I used this book for more than one class. What should I do?</strong><br/>
Make one listing, but put all the course titles and course numbers into their respective fields so that other students can search for it.</p>
<p><strong>What happens after I send a message?</strong><br/>
Your message will be sent to the seller/buyer's Penn email account. Any further correspondence will occur in your own mailboxes, outside of Penn Book Bazaar.</p>
<p><strong>Who created or is responsible for the textbook exchange?</strong><br/>
Penn Book Bazaar was created by the UA over Summer 2010. It is maintained with the assistance of the Interfraternity Council. You can reach the moderators of the system at textbook@pennua.org.

For more information, please see <a href="http://pennbookbazaar.com/acknowledgments.htm">Acknowledgments</a>. </p>

